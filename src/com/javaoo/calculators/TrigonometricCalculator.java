package com.javaoo.calculators;

/**
 * @author waris
 *
 */
public class TrigonometricCalculator {
	/*
	 * Method to perform Sine functionality
	 */
	public double sine(double x) {
		return Math.sin(x);
	}

	/*
	 * Method to perform cosine functionality
	 */
	public double cosine(double x) {
		return Math.cos(x);
	}

	/*
	 * Method to perform tangent functionality
	 */
	public double tangent(double x) {
		return Math.tan(x);
	}

	/*
	 * Method to perform arcsine functionality
	 */
	public double arcsine(double x) {
		return Math.asin(x);
	}

	/*
	 * Method to perform arccosine functionality
	 */
	public double arccosine(double x) {
		return Math.acos(x);
	}

	/*
	 * Method to perform arctangent functionality
	 */
	public double arctangent(double x) {
		return Math.atan(x);
	}
}
